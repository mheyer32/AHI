
	.MACRO	LONG num
.ifdef	LITTLE_ENDIAN
	.byte	((\num)>>24)&255
	.byte	((\num)>>16)&255
	.byte	((\num)>> 8)&255
	.byte	((\num)>> 0)&255
.else
	.long	\num
.endif
	.ENDM

	.MACRO	LONG2 num1,num2
.ifdef	LITTLE_ENDIAN
	.byte	((\num1)>>24)&255
	.byte	((\num1)>>16)&255
	.byte	((\num1)>> 8)&255
	.byte	((\num1)>> 0)&255
	
	.byte	((\num2)>>24)&255
	.byte	((\num2)>>16)&255
	.byte	((\num2)>> 8)&255
	.byte	((\num2)>> 0)&255
.else
	.long	\num1, \num2
.endif
	.ENDM

	.MACRO	FORM_START name
CHUNKCNT	.ASSIGNA 0
	.ascii	"FORM"
	LONG	FORMEND-FORMSTART
FORMSTART:
	.ascii	"\name"
	.ENDM

	.MACRO	FORM_END name
FORMEND:
	.balign	2,0
	.ENDM

	.MACRO	CHUNK_START name
	.ascii	"\name"
	LONG	CHUNKEND\&CHUNKCNT-CHUNKSTART\&CHUNKCNT
CHUNKSTART\&CHUNKCNT:
	.ENDM

	.MACRO	CHUNK_END
CHUNKEND\&CHUNKCNT:
CHUNKCNT	.ASSIGNA \&CHUNKCNT+1
	.balign	2,0
	.ENDM

